#Casting trong python để convert kiểu dữ liệu sang một kiểu dữ liệu khác

x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3

#Trả về kết quả là số nguyên
print(x)
print(y)
print(z)



#Trả về kết quả là một số thực kiểu float
a = float(1)     # x will be 1.0
b = float(2.8)   # y will be 2.8
c = float("3")   # z will be 3.0
d = float("4.2") # w will be 4.2

print(a)
print(b)
print(c)
print(d)

